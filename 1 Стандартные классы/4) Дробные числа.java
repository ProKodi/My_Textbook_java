




public class Main{
public 
    static void main(String args[]){
        // Дробные числа
        // float - 4 байт (Значения: одинарная точность)
        float number_flt = 23.6f;  // Так создается переменная с дробным численым типом данных
        System.out.println(number_flt);

        // double - 8 байт (Значения: двойная точность)
        double number_dbl = 45.9;  // Так создается переменная с дробным численым типом данных
        System.out.println(number_dbl);
    }
}
