




public class Main{
public 
    static void main(String args[]){
        /* Escape - последовательности - данные штуки используются во всех языках. Необходимы для текста
    
        \b - Удаление последнего выделеного символа
        \n - enter
        \t - tab

        \\ - Вывести \ (обратную черту)
        \" - Вывести кавычку "
        \' - Вывести кавычку '

        */

        // char - тип данных в который помещается 1 символ
        char my = 'i'; // Так создается переменная с символом 
        System.out.println(my);
        // Перевод символа в число 
        System.out.println((int)my);

        String name = "Alex"; // Так создается переменная с текстом
        System.out.println(name);
    }
}
